// Bundles the 3 official Graphnosis demo skill packs into the sidecar.
//
// Reads dist/packs/bundle/*.gsk (produced by `scripts/build-gsk.mjs` when a
// pack is authored with `bundle: true`) and writes
// `src/skill-demos.generated.ts` — a plain data module compiled into the
// sidecar binary. On first cortex unlock (or app-version bump), the sidecar
// auto-creates a `Skill Demos` engram and imports these three .gsk packs
// via the same signature-verifying pipeline that `skill:importGsk` uses for
// user-loaded packs. So the demos ship offline, are tamper-detectable, and
// surface in the Skills library exactly as any user-imported skill would.
//
// Run automatically by `pnpm build` AFTER `node scripts/build-gsk.mjs --sign`
// has produced fresh bundle/ files. Safe to run by hand any time.

import { readdirSync, readFileSync, writeFileSync, existsSync, statSync, mkdirSync } from 'node:fs';
import { join, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const SIDECAR_DIR = fileURLToPath(new URL('..', import.meta.url));
const REPO_ROOT = join(SIDECAR_DIR, '..', '..');
const BUNDLE_DIR = join(REPO_ROOT, 'dist', 'packs', 'bundle');
const OUT_FILE = join(SIDECAR_DIR, 'src', 'skill-demos.generated.ts');

const banner =
  '// AUTO-GENERATED by scripts/generate-skill-demos-content.mjs — DO NOT EDIT.\n' +
  '// Three signed Graphnosis demo .gsk packs base64-embedded into the sidecar\n' +
  '// so the App can ingest them into a `Skill Demos` engram offline on first\n' +
  '// cortex unlock. Regenerate via: node scripts/build-gsk.mjs --sign &&\n' +
  '//                                 node apps/desktop-sidecar/scripts/generate-skill-demos-content.mjs\n\n' +
  'export interface BundledSkillDemo {\n' +
  '  /** Filename of the .gsk on disk under dist/packs/bundle/. Drives ordering\n' +
  '   *  and the snapshot-history label users see. */\n' +
  '  filename: string;\n' +
  '  /** Pack id parsed from the filename — matches the `id` field in the\n' +
  '   *  authored pack literal. Stable across rebuilds; used to detect "this\n' +
  '   *  same skill has been re-imported" for the version-notify flow. */\n' +
  '  id: string;\n' +
  '  /** Base64-encoded .gsk payload. Decrypted + verified at ingest time by\n' +
  '   *  the same parseGskPackage + verifyGskSignature path that handles\n' +
  '   *  user-loaded .gsk files. */\n' +
  '  gskBase64: string;\n' +
  '}\n\n';

// ── Collect bundle/*.gsk ─────────────────────────────────────────────────────

if (!existsSync(BUNDLE_DIR)) {
  // No bundle dir means build-gsk.mjs hasn't run yet, or no packs are marked
  // bundle:true. Emit an EMPTY array so the sidecar TypeScript still compiles.
  // The smoke test fails when this array is empty in CI, so the cost of
  // forgetting to rebuild packs is visible immediately.
  console.warn(`[generate-skill-demos] ${BUNDLE_DIR} not found — emitting empty BUNDLED_SKILL_DEMOS.`);
  writeFileSync(OUT_FILE, banner + 'export const BUNDLED_SKILL_DEMOS: BundledSkillDemo[] = [];\n');
  process.exit(0);
}

const files = readdirSync(BUNDLE_DIR)
  .filter((n) => n.endsWith('.gsk'))
  .filter((n) => statSync(join(BUNDLE_DIR, n)).isFile())
  .sort(); // alphabetical → deterministic compile output

if (files.length === 0) {
  console.warn(`[generate-skill-demos] no .gsk files in ${BUNDLE_DIR} — emitting empty array.`);
  writeFileSync(OUT_FILE, banner + 'export const BUNDLED_SKILL_DEMOS: BundledSkillDemo[] = [];\n');
  process.exit(0);
}

// ── Encode + emit ────────────────────────────────────────────────────────────

const entries = files.map((filename) => {
  const bytes = readFileSync(join(BUNDLE_DIR, filename));
  // Filename shape: `<pack-id>-graphnosis-demo-skill.gsk`. Strip the suffix
  // to recover the pack id. Falls back to the bare filename for safety.
  const SUFFIX = '-graphnosis-demo-skill.gsk';
  const id = filename.endsWith(SUFFIX) ? filename.slice(0, -SUFFIX.length) : basename(filename, '.gsk');
  return {
    filename,
    id,
    gskBase64: bytes.toString('base64'),
  };
});

// Pretty-print: each entry on its own block, base64 is the wide field so we
// keep the others compact for human readability of the generated file.
const body = entries.map((e) => {
  return (
    '  {\n' +
    `    filename: ${JSON.stringify(e.filename)},\n` +
    `    id: ${JSON.stringify(e.id)},\n` +
    `    gskBase64: ${JSON.stringify(e.gskBase64)},\n` +
    '  },'
  );
}).join('\n');

writeFileSync(
  OUT_FILE,
  banner +
  'export const BUNDLED_SKILL_DEMOS: BundledSkillDemo[] = [\n' +
  body + '\n' +
  '];\n',
);

const totalBytes = entries.reduce((n, e) => n + Buffer.byteLength(e.gskBase64, 'utf8'), 0);
console.log(`[generate-skill-demos] bundled ${entries.length} skill demo pack(s) → src/skill-demos.generated.ts (${(totalBytes / 1024).toFixed(1)} kB base64)`);
