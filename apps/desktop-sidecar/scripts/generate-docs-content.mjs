// Bundles the Graphnosis documentation into the sidecar.
//
// Reads every Markdown page under apps/docs and writes
// src/docs-content.generated.ts — a plain data module compiled straight into
// the sidecar. The docs therefore ship *inside* the app: the "add the
// Graphnosis docs to your Cortex" feature ingests them with zero network
// access, so it works offline (Wi-Fi off) and never contacts any server.
//
// Run automatically by `pnpm build`. Safe to run by hand any time.

import { readdirSync, readFileSync, writeFileSync, existsSync, statSync } from 'node:fs';
import { join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const SIDECAR_DIR = fileURLToPath(new URL('..', import.meta.url));
const DOCS_ROOT = join(SIDECAR_DIR, '..', 'docs', 'src', 'content', 'docs');
const OUT_FILE = join(SIDECAR_DIR, 'src', 'docs-content.generated.ts');

/** Recursively collect every `.md` file under `dir`. `.mdx` is skipped — the
 *  only one is the splash homepage, which is JSX, not documentation prose. */
function collectMarkdown(dir) {
  const found = [];
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    if (statSync(full).isDirectory()) found.push(...collectMarkdown(full));
    else if (name.endsWith('.md')) found.push(full);
  }
  return found;
}

/** Split YAML frontmatter from the Markdown body; pull out `title`. */
function parseFrontmatter(raw) {
  const m = raw.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  if (!m) return { title: '', body: raw };
  const body = raw.slice(m[0].length);
  const titleMatch = m[1].match(/^title:\s*(.+)$/m);
  const title = titleMatch ? titleMatch[1].trim().replace(/^['"]|['"]$/g, '') : '';
  return { title, body };
}

const banner =
  '// AUTO-GENERATED by scripts/generate-docs-content.mjs — DO NOT EDIT.\n' +
  '// Bundles the Graphnosis documentation so the app can ingest it into a\n' +
  '// `graphnosis-docs` engram fully offline, with no network access.\n\n' +
  'export interface BundledDoc {\n' +
  '  /** Path under apps/docs/src/content/docs, without extension. */\n' +
  '  slug: string;\n' +
  '  /** Page title from the frontmatter. */\n' +
  '  title: string;\n' +
  '  /** Markdown body, frontmatter stripped. */\n' +
  '  markdown: string;\n' +
  '}\n\n';

let docs = [];
if (existsSync(DOCS_ROOT)) {
  docs = collectMarkdown(DOCS_ROOT)
    .sort()
    .map((file) => {
      const { title, body } = parseFrontmatter(readFileSync(file, 'utf8'));
      return {
        slug: relative(DOCS_ROOT, file).replace(/\.md$/, ''),
        title,
        markdown: body.trim(),
      };
    })
    .filter((d) => d.markdown.length > 0);
} else {
  console.warn(`[generate-docs-content] docs not found at ${DOCS_ROOT} — writing an empty bundle`);
}

writeFileSync(
  OUT_FILE,
  banner + `export const BUNDLED_DOCS: BundledDoc[] = ${JSON.stringify(docs, null, 2)};\n`,
);
console.log(`[generate-docs-content] bundled ${docs.length} docs page(s) → ${relative(SIDECAR_DIR, OUT_FILE)}`);
